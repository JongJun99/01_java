package com.ohgiraffers.section01.literal;

public class Application1 {

    public static void main(String[] args) {

        System.out.println(123);
        System.out.println(1.23);
        System.out.println('a'); // 문자 형태의 값은 홀따옴표 ''

//        System.out.println('ab'); //두 개 이상은 문자로 취급하지 않기 때문에 에러 발생
//        System.out.println(''); // 싱글 따옴표는 에러가나옴

        System.out.println('1'); //홀따옴표 안에 있기때문에 문자 1임
        System.out.println("안녕하세요");
        System.out.println("123");
        System.out.println(""); // 아무 값도 없는 쌍 따옴표도 문자열로 취급
        System.out.println("a"); //'a' = 문자 , "a" 문자열임

        System.out.println(true); //true 혹은 false 값을 논리형이라고 한다
        System.out.println(false); //true 혹은 false 값을 논리형이라고 한다












    }
}
